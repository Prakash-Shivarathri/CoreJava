package Basic;

public class NewLineOutPut {
    // Function to print each word in a single line
    static void printIndividualLine(){
        System.out.println("Geeks"+'\n'+"for"+'\n'+"Geeks");
    }
    public static void main(String[] args){
        // Creating object of class to access the function
        NewLineOutPut N = new NewLineOutPut();
        // calling the function...
        N.printIndividualLine();
    }

}
